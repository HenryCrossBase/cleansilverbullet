export default function CountrySelector({
    value,
    onChange,
}: {
    value: string;
    onChange: (val: string) => void;
}) {
    const [isOpen, setIsOpen] = useState(false);
    const [search, setSearch] = useState("");
    const dropdownRef = useRef<HTMLDivElement>(null);

    useEffect(() => {
        const handleClickOutside = (event: MouseEvent) => {
            if (dropdownRef.current && !dropdownRef.current.contains(event.target as Node)) {
                setIsOpen(false);
            }
        };
        document.addEventListener("mousedown", handleClickOutside);
        return () => document.removeEventListener("mousedown", handleClickOutside);
    }, []);

    const selectedCountry =
        COUNTRIES.find((c) => c.code === value) || COUNTRIES[0];

    const getFlag = (code: string) => {
        if (code === "All")
            return <span className="text-base leading-none">🌍</span>;
        if (code === "Global")
            return <span className="text-base leading-none">🌐</span>;
        return (
            <Image
                src={`https://flagcdn.com/w20/${code.toLowerCase()}.png`}
                alt={code}
                width={16}
                height={12}
                sizes="16px"
                className="h-3 w-4 rounded-sm object-cover"
            />
        );
    };

    const filteredCountries = COUNTRIES.filter(c => 
        c.name.toLowerCase().includes(search.toLowerCase()) || 
        c.code.toLowerCase().includes(search.toLowerCase())
    );

    return (
        <div className="relative w-full" ref={dropdownRef}>
            <Button
                type="button"
                variant="outline"
                role="combobox"
                aria-expanded={isOpen}
                className="h-10 min-w-40 w-full justify-between px-3 border-border/50 bg-background/50 hover:bg-background/80"
                onClick={() => setIsOpen(!isOpen)}
            >
                <div className="flex min-w-0 items-center gap-2 text-foreground">
                    {getFlag(selectedCountry.code)}
                    <span className="truncate">{selectedCountry.name}</span>
                </div>
                <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
            </Button>

            {isOpen && (
                <div className="absolute top-full left-0 mt-2 w-full min-w-60 bg-popover border border-border/50 rounded-md shadow-lg z-[9999] overflow-hidden flex flex-col pointer-events-auto">
                    <div className="p-2 border-b border-border/30 flex items-center gap-2 bg-background/50">
                        <Search className="h-4 w-4 text-muted-foreground ml-1" />
                        <input 
                            type="text" 
                            placeholder="Search region..." 
                            className="w-full bg-transparent border-none outline-none text-sm text-foreground placeholder:text-muted-foreground h-8"
                            value={search}
                            onChange={(e) => setSearch(e.target.value)}
                            autoFocus
                        />
                    </div>
                    <div className="max-h-72 overflow-y-auto p-1 flex flex-col bg-popover">
                        {filteredCountries.length === 0 && (
                            <div className="py-6 text-center text-sm text-muted-foreground">No regions found</div>
                        )}
                        {filteredCountries.map((c) => (
                            <div
                                key={c.code}
                                className={cn(
                                    "flex items-center gap-2 w-full cursor-pointer px-2 py-2 text-sm rounded-sm text-foreground hover:bg-accent hover:text-accent-foreground transition-colors",
                                    value === c.code && "bg-accent text-accent-foreground"
                                )}
                                onClick={() => {
                                    onChange(c.code);
                                    setIsOpen(false);
                                    setSearch("");
                                }}
                            >
                                {getFlag(c.code)}
                                <span>{c.name}</span>
                                {value === c.code && <Check className="ml-auto h-4 w-4" />}
                            </div>
                        ))}
                    </div>
                </div>
            )}
        </div>
    );
}
